
function showColors() {
    if($("#dropdown-colors-content").is(":visible")){
        $('#dropdown-colors-content').hide();
    } else {
        $('#dropdown-colors-content').show();
    }
}

function showPlugins() {
    if($("#dropdown-plugins-content").is(":visible")){
        $('#dropdown-plugins-content').hide();
    } else {
        $('#dropdown-plugins-content').show();
    }
}